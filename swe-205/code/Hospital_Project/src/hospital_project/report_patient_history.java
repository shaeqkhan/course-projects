    private void button_reportActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_reportActionPerformed
        // reading name of patient from the text field.
        String patient = textField_patient.getText().trim();

        textArea_report.append("\nFile Number" + "\t" + "Pateint Name" + "\t" + "Patient History" + "\n\n");
        
        // condition to check if the field is left blank.
        if(patient.equals("")){
            JOptionPane.showMessageDialog(null, "Field is blank.\nPlease enter the doctor's name.","Error", JOptionPane.ERROR_MESSAGE);
        }
        else{
            try{
                // Creating new array list of type "Patient" and storing info from the file using "Control" class.
                ArrayList<Patient> pat = new ArrayList<Patient>();

                pat = Control.importPatientDatabase();

                // for each loop to locate the patient in the array list.
                for(Patient p:pat){
                    if(patient.equals(p.getName())){
                        // displaying the patient information in the text area.
                        textArea_report.append(p.getFileNumber() + "\t" + p.getName() + "\t" + p.getPatientHistory() + "\n" );
                    }
                }

            }
            // in case of any exception error the following is displayed.
            catch(Exception e){
                JOptionPane.showMessageDialog(null, "Unexpected Error.\nCould not generate report.","Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_button_reportActionPerformed

    // When the admin wants to print the report the following action takes place.
    private void button_printActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_printActionPerformed
        String patient = textField_patient.getText().trim();

        PrintWriter output=null;

     	try
     	{
     		output=new PrintWriter(new File(patient + " - report.txt"));
     	}
     	catch(FileNotFoundException e)
     	{
     		JOptionPane.showMessageDialog(null, "Unable to create report.", "Error", JOptionPane.ERROR_MESSAGE);
     	}

        ArrayList<Patient> pat = new ArrayList<Patient>();
        pat = Control.importPatientDatabase();

        output.println("File Number" + "\t" + "Patient Name" + "\t" + "Patient History" + "\n\n");

		for(Patient p:pat)
      	{
      		if(patient.equals(p.getName())){

      			output.print(p.getFileNumber()+ "\t" + p.getName() + "\t" + p.getPatientHistory());
      			output.println();
      		}

      	}
      	output.close();
        JOptionPane.showMessageDialog(null, "Report has been generated.\n Check file - " + patient + ".txt to print it.");
    }//GEN-LAST:event_button_printActionPerformed

