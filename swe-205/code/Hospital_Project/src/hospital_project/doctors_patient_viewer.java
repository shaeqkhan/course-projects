    /* When the doctor is done with the chages he/she wants to make to a patiennt's
     * history, then they press the "Save" button that allows them to save the
     * changes made to the patient history.
     */
    private void button_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_saveActionPerformed
        
        // Read the whole patient history into a String variable.
        String patientHistory = textArea_patientHistory.getText();

        // Boolean variable to show that the change to the patient history is made.
        boolean flag=false;

        // Gettin the pateint name and filenumber from the GUI.
        String pName = textField_name.getText().trim();
        String file = textField_fileNumber.getText().trim();

        // Initializing a new Array List to store Patient info from database.
        ArrayList<Patient> pat = new ArrayList<Patient>();

        // Importing the entire patient databse to the initialized Array List.
        pat = Control.importPatientDatabase();

        /* Looop to check if the patient is in the databse and then make the
         * change that will be recorded in a boolean variable.
         */
        
        for(Patient p:pat)
      	{
      		// Checking for a patient match from database to the one from GUI using name and file number.
            if(pName.equals(p.getName()) && file.equals(p.getFileNumber())){
                
                // Changing the patient history to the current one from GUI.
                p.setPatientHistory(patientHistory);

                // Changing the status of the boolean variable.
                flag=true;
            }
      	}
        Control.printPatientDatabase(pat);
        JOptionPane.showMessageDialog(null, "Pateint History successfully updated.");

        // If the patient is not found then no change in the boolean variable shows that patient was not found.
        if(!flag){
            JOptionPane.showMessageDialog(null, "Patient does not exist in databse.\n Kick him out of the hospital please.", "Error", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_button_saveActionPerformed

